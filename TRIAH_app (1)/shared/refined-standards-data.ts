export function getRefinedStandardsSummary() {
  return REFINED_STANDARDS.map(s => {
    const totalFactors = s.elements.reduce((sum, e) => sum + e.factors.length, 0);
    const mustPassElements = s.elements.filter(e => e.mustPass).length;
    const mustPassFactors = s.elements.reduce(
      (sum, e) => sum + e.factors.filter(f => f.mustPass).length, 0
    );
    return {
      id: s.id,
      number: s.number,
      name: s.name,
      shortName: s.shortName,
      weight: s.weight,
      domain: s.domain,
      elementCount: s.elements.length,
      factorCount: totalFactors,
      mustPassElements,
      mustPassFactors,
    };
  });
}

export function getTotalFactorCount() {
  return REFINED_STANDARDS.reduce(
    (sum, s) => sum + s.elements.reduce((eSum, e) => eSum + e.factors.length, 0), 0
  );
}
